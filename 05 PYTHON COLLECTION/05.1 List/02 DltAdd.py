
lst = [7,4,2,6,4,3]

# add 100 in last
lst.append(100)
print(lst)

# insert before a number
lst.insert(2,200)
print(lst)

# remove any number
lst.remove(6)
print(lst)

# pop last number
lst.pop()
var = lst
print(var)

# delete number of index
del lst[3]
print(lst) 

# clear list
lst.clear()
print(lst)

# delete full list
del lst
print(lst)

